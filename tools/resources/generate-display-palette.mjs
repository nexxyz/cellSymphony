import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { exit } from "node:process";

const root = resolve(import.meta.dirname, "..", "..");
const sourcePath = resolve(root, "resources", "display-palette.json");
const tsOutputPath = resolve(root, "packages", "device-contracts", "src", "displayPalette.generated.ts");
const cssOutputPath = resolve(root, "apps", "desktop", "src", "ui", "displayPalette.generated.css");
const printCssOutputPath = resolve(root, "userdocs", "print", "displayPalette.generated.css");
const check = process.argv.includes("--check");

const entries = [
  ["green", "Green"],
  ["red", "Red"],
  ["blue", "Blue"],
  ["yellow", "Yellow"],
  ["gray", "Gray"],
  ["white", "White"],
  ["black", "Black"]
];

function rgbFromHex(key, value) {
  if (typeof value !== "string" || !/^#[0-9A-Fa-f]{6}$/.test(value)) {
    throw new Error(`Invalid display palette '${key}': expected #RRGGBB, got ${String(value)}`);
  }
  return [1, 3, 5].map((index) => Number.parseInt(value.slice(index, index + 2), 16));
}

function rgb565([r, g, b]) {
  return ((r & 0xf8) << 8) | ((g & 0xfc) << 3) | (b >> 3);
}

const source = JSON.parse(readFileSync(sourcePath, "utf8"));
const expectedKeys = entries.map(([key]) => key).sort();
const actualKeys = Object.keys(source).sort();
if (JSON.stringify(actualKeys) !== JSON.stringify(expectedKeys)) {
  throw new Error(`Invalid display palette keys: expected ${expectedKeys.join(", ")}; got ${actualKeys.join(", ")}`);
}
const palette = Object.fromEntries(entries.map(([key, label]) => {
  const hex = source[key];
  const rgb = rgbFromHex(key, hex);
  return [key, { label, hex: hex.toUpperCase(), rgb, rgb565: rgb565(rgb) }];
}));

const tsContent = `// Generated by tools/resources/generate-display-palette.mjs from resources/display-palette.json.
// Do not edit by hand.

export type DisplayPaletteRgb = readonly [number, number, number];

export const DISPLAY_PALETTE = ${JSON.stringify(palette, null, 2)} as const;

export const GREEN_COLOR = DISPLAY_PALETTE.green.rgb;
export const RED_COLOR = DISPLAY_PALETTE.red.rgb;
export const BLUE_COLOR = DISPLAY_PALETTE.blue.rgb;
export const YELLOW_COLOR = DISPLAY_PALETTE.yellow.rgb;
export const GRAY_COLOR = DISPLAY_PALETTE.gray.rgb;
export const WHITE_COLOR = DISPLAY_PALETTE.white.rgb;
export const BLACK_COLOR = DISPLAY_PALETTE.black.rgb;
`;

const cssContent = `/* Generated by tools/resources/generate-display-palette.mjs from resources/display-palette.json. */
/* Do not edit by hand. */

:root {
${entries.map(([key]) => `  --palette-${key}-rgb: ${palette[key].rgb.join(" ")};`).join("\n")}
${entries.map(([key]) => `  --palette-${key}: rgb(var(--palette-${key}-rgb));`).join("\n")}
}
`;

if (check) {
  const existing = readFileSync(tsOutputPath, "utf8");
  if (existing !== tsContent) {
    console.error(`${tsOutputPath} is stale. Run: corepack pnpm run palette:generate`);
    exit(1);
  }
  const existingCss = readFileSync(cssOutputPath, "utf8");
  if (existingCss !== cssContent) {
    console.error(`${cssOutputPath} is stale. Run: corepack pnpm run palette:generate`);
    exit(1);
  }
  const existingPrintCss = readFileSync(printCssOutputPath, "utf8");
  if (existingPrintCss !== cssContent) {
    console.error(`${printCssOutputPath} is stale. Run: corepack pnpm run palette:generate`);
    exit(1);
  }
} else {
  writeFileSync(tsOutputPath, tsContent, "utf8");
  writeFileSync(cssOutputPath, cssContent, "utf8");
  writeFileSync(printCssOutputPath, cssContent, "utf8");
}
