import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { exit } from "node:process";

const root = resolve(import.meta.dirname, "..");
const sourcePath = resolve(root, "resources", "platform-capabilities.json");
const outputPath = resolve(root, "packages", "device-contracts", "src", "platformCapabilities.generated.ts");
const check = process.argv.includes("--check");

function positiveInteger(source, key) {
  const value = Number(source[key]);
  if (!Number.isInteger(value) || value <= 0) {
    throw new Error(`Invalid platform capability '${key}': expected positive integer, got ${String(source[key])}`);
  }
  return value;
}

function capabilitiesFrom(source) {
  const scanSectionCounts = source.scanSectionCounts;
  if (!Array.isArray(scanSectionCounts) || scanSectionCounts.length === 0) {
    throw new Error("Invalid platform capability 'scanSectionCounts': expected non-empty array");
  }
  const parsedScanSectionCounts = scanSectionCounts.map((entry) => {
    const value = Number(entry);
    if (!Number.isInteger(value) || value <= 0) {
      throw new Error(`Invalid scanSectionCounts entry: ${String(entry)}`);
    }
    return value;
  });
  return {
    gridWidth: positiveInteger(source, "gridWidth"),
    gridHeight: positiveInteger(source, "gridHeight"),
    partCount: positiveInteger(source, "partCount"),
    instrumentCount: positiveInteger(source, "instrumentCount"),
    sampleSlotCount: positiveInteger(source, "sampleSlotCount"),
    busCount: positiveInteger(source, "busCount"),
    globalFxSlotCount: positiveInteger(source, "globalFxSlotCount"),
    auxEncoderCount: positiveInteger(source, "auxEncoderCount"),
    touchFxMaxConcurrent: positiveInteger(source, "touchFxMaxConcurrent"),
    scanSectionCounts: parsedScanSectionCounts,
    panPositionCount: positiveInteger(source, "panPositionCount"),
    oledWidth: positiveInteger(source, "oledWidth"),
    oledHeight: positiveInteger(source, "oledHeight")
  };
}

const caps = capabilitiesFrom(JSON.parse(readFileSync(sourcePath, "utf8")));
const content = `// Generated by tools/generate-platform-capabilities.mjs from resources/platform-capabilities.json.
// Do not edit by hand.

export const PLATFORM_CAPS = ${JSON.stringify(caps, null, 2)} as const;

export const GRID_WIDTH = PLATFORM_CAPS.gridWidth;
export const GRID_HEIGHT = PLATFORM_CAPS.gridHeight;
export const PAN_POSITION_COUNT = PLATFORM_CAPS.panPositionCount;
export const AUX_ENCODER_COUNT = PLATFORM_CAPS.auxEncoderCount;
export const OLED_WIDTH = PLATFORM_CAPS.oledWidth;
export const OLED_HEIGHT = PLATFORM_CAPS.oledHeight;
`;

if (check) {
  const existing = readFileSync(outputPath, "utf8");
  if (existing !== content) {
    console.error(`${outputPath} is stale. Run: corepack pnpm run capabilities:generate`);
    exit(1);
  }
} else {
  writeFileSync(outputPath, content, "utf8");
}
